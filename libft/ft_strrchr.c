#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *s2;

	s2 = (char *)s + ft_strlen(s);
	while (*s2 != c)
	{
		if (s2 == s)
		{
			return (NULL);
		}
		s2--;
	}
	return (s2);
}
